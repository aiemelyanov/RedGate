CREATE TABLE apex_030200.wwv_mig_rpt_datasrc (
  "ID" NUMBER NOT NULL,
  data_id NUMBER NOT NULL,
  security_group_id NUMBER NOT NULL,
  "NAME" VARCHAR2(4000 BYTE),
  defaultgroupname VARCHAR2(4000 BYTE),
  maxrows VARCHAR2(4000 BYTE),
