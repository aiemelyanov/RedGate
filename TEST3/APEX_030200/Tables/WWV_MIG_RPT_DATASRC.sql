  assignee VARCHAR2(255 BYTE),
  notes VARCHAR2(4000 BYTE),
  tags VARCHAR2(4000 BYTE),
  created_on DATE,
  created_by VARCHAR2(400 BYTE),
  last_updated_on DATE,
  last_updated_by VARCHAR2(400 BYTE),
  CONSTRAINT wwv_mig_repsrc_pk PRIMARY KEY ("ID"),
  CONSTRAINT wwv_mig_repsrc_id_fk FOREIGN KEY (data_id) REFERENCES apex_030200.wwv_mig_rpt_data ("ID") ON DELETE CASCADE,
